class Bitman:


# ----------------------------------------------
# ** def __init__ : 
# ----------------------------------------------
    def __init__(self, l):
        # super().__init__()
        self._value = [False for i in range(l)]


# ----------------------------------------------
# ** def __getitem__ : 
# ----------------------------------------------
    def __getitem__(self, i):
        return self._value[i]


# ----------------------------------------------
# ** def __setitem__ : 
# ----------------------------------------------
    def __setitem__(self, i, v):
        self._value[i] = v


# ----------------------------------------------
# ** def __len__ : 
# ----------------------------------------------
    def __len__(self):
        return len(self._value)


# ----------------------------------------------


# -------------------------------------------- #
# ** def __call__(self) : 
# ----------------------------------------------
    def __call__(self):
        a = 0
        for i in range(len(self._value)):
            a = a | self._value[i]<<i
        return a


